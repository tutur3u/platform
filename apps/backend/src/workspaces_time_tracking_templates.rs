use serde::{Deserialize, Serialize};
use serde_json::{Value, json};

use crate::{
    APPLICATION_JSON, BackendConfig, BackendRequest, BackendResponse, contact, json_response,
    method_not_allowed, no_store_response,
    outbound::{OutboundHttpClient, OutboundMethod, OutboundRequest, OutboundResponse},
    supabase_auth,
};

const PATH_PREFIX: &str = "/api/v1/workspaces/";
const PATH_SUFFIX: &str = "/time-tracking/templates";

const ROOT_WORKSPACE_ID: &str = "00000000-0000-0000-0000-000000000000";
const INTERNAL_WORKSPACE_SLUG: &str = "internal";
const PERSONAL_WORKSPACE_SLUG: &str = "personal";

const UNAUTHORIZED_MESSAGE: &str = "Unauthorized";
const WORKSPACE_ACCESS_DENIED_MESSAGE: &str = "Workspace access denied";
const MEMBERSHIP_LOOKUP_FAILED_MESSAGE: &str = "Failed to verify workspace membership";
const INTERNAL_SERVER_ERROR_MESSAGE: &str = "Internal server error";

/// Row returned by the recent-sessions query. Embedded `category` and `task`
/// are kept as opaque JSON to mirror the legacy `category:...(*), task:...(*)`
/// selects, which return the full related rows verbatim.
#[derive(Deserialize)]
struct RecentSessionRow {
    title: Option<String>,
    description: Option<String>,
    category_id: Option<String>,
    task_id: Option<String>,
    #[serde(default)]
    category: Value,
    #[serde(default)]
    task: Value,
}

#[derive(Serialize)]
struct TemplateOut {
    title: Option<String>,
    description: Option<String>,
    category_id: Option<String>,
    task_id: Option<String>,
    category: Value,
    task: Value,
    usage_count: u64,
}

#[derive(Deserialize)]
struct WorkspaceIdRow {
    id: Option<String>,
}

#[derive(Deserialize)]
struct WorkspaceMembershipRow {
    #[serde(rename = "type")]
    membership_type: Option<String>,
}

pub(crate) async fn handle_workspaces_time_tracking_templates_route(
    config: &BackendConfig,
    request: BackendRequest<'_>,
    outbound: &impl OutboundHttpClient,
) -> Option<BackendResponse> {
    let raw_ws_id = templates_ws_id(request.path)?;

    Some(match request.method {
        "GET" => templates_response(config, request, raw_ws_id, outbound).await,
        method => no_store_response(method_not_allowed(method, "GET")),
    })
}

async fn templates_response(
    config: &BackendConfig,
    request: BackendRequest<'_>,
    raw_ws_id: &str,
    outbound: &impl OutboundHttpClient,
) -> BackendResponse {
    let contact_data = &config.contact_data;

    let Some(access_token) = supabase_auth::request_access_token(request) else {
        return error_response(401, UNAUTHORIZED_MESSAGE);
    };
    let Some(user_id) =
        supabase_auth::fetch_supabase_auth_user(contact_data, &access_token, outbound)
            .await
            .and_then(|user| user.id.filter(|id| !id.trim().is_empty()))
    else {
        return error_response(401, UNAUTHORIZED_MESSAGE);
    };

    let resolved_ws_id =
        match normalize_workspace_id(contact_data, outbound, raw_ws_id, &user_id, &access_token)
            .await
        {
            Ok(ws_id) => ws_id,
            Err(()) => return error_response(500, MEMBERSHIP_LOOKUP_FAILED_MESSAGE),
        };

    // Verify workspace access. A lookup failure maps to 500, a non-member to
    // 403, matching the legacy `membership_lookup_failed` / `!ok` branches.
    match verify_workspace_member(contact_data, outbound, &resolved_ws_id, &user_id).await {
        Ok(true) => {}
        Ok(false) => return error_response(403, WORKSPACE_ACCESS_DENIED_MESSAGE),
        Err(()) => return error_response(500, MEMBERSHIP_LOOKUP_FAILED_MESSAGE),
    }

    let sessions = match recent_sessions(
        contact_data,
        outbound,
        &resolved_ws_id,
        &user_id,
        &access_token,
    )
    .await
    {
        Ok(sessions) => sessions,
        Err(()) => return error_response(500, INTERNAL_SERVER_ERROR_MESSAGE),
    };

    let templates = build_templates(sessions);

    no_store_response(json_response(200, json!({ "templates": templates })))
}

/// Fetch the 10 most recent finished sessions for the user in the workspace,
/// mirroring the legacy query (is_running = false, duration_seconds not null).
/// Read through the caller's token so RLS applies exactly like the legacy
/// authed supabase client did.
async fn recent_sessions(
    contact_data: &contact::ContactDataConfig,
    outbound: &impl OutboundHttpClient,
    ws_id: &str,
    user_id: &str,
    access_token: &str,
) -> Result<Vec<RecentSessionRow>, ()> {
    let select = "title,description,category_id,task_id,\
category:time_tracking_categories(*),task:tasks(*)";

    let Some(url) = contact_data.rest_url(
        "time_tracking_sessions",
        &[
            ("select", select.to_owned()),
            ("ws_id", format!("eq.{ws_id}")),
            ("user_id", format!("eq.{user_id}")),
            ("is_running", "eq.false".to_owned()),
            ("duration_seconds", "not.is.null".to_owned()),
            ("order", "created_at.desc".to_owned()),
            ("limit", "10".to_owned()),
        ],
    ) else {
        return Err(());
    };

    let response = send_caller_rest_request(contact_data, outbound, &url, access_token).await?;

    if !(200..300).contains(&response.status) {
        return Err(());
    }

    response.json::<Vec<RecentSessionRow>>().map_err(|_| ())
}

/// Group sessions by (title, category_id, task_id), count usages, sort by
/// usage descending, and keep the top 5. Insertion order is preserved for the
/// initial grouping (matching the JS `Map` iteration order) before the stable
/// sort, so ties retain first-seen order.
fn build_templates(sessions: Vec<RecentSessionRow>) -> Vec<TemplateOut> {
    let mut keys: Vec<String> = Vec::new();
    let mut templates: Vec<TemplateOut> = Vec::new();

    for session in sessions {
        let key = format!(
            "{}-{}-{}",
            session.title.as_deref().unwrap_or(""),
            session.category_id.as_deref().unwrap_or(""),
            session.task_id.as_deref().unwrap_or(""),
        );

        if let Some(index) = keys.iter().position(|existing| existing == &key) {
            templates[index].usage_count += 1;
        } else {
            keys.push(key);
            templates.push(TemplateOut {
                title: session.title,
                description: session.description,
                category_id: session.category_id,
                task_id: session.task_id,
                category: session.category,
                task: session.task,
                usage_count: 1,
            });
        }
    }

    // Stable sort by usage_count descending (preserves first-seen order on ties,
    // matching the JS Array.from(...).sort behavior for equal usage counts).
    templates.sort_by_key(|template| std::cmp::Reverse(template.usage_count));
    templates.truncate(5);

    templates
}

async fn normalize_workspace_id(
    contact_data: &contact::ContactDataConfig,
    outbound: &impl OutboundHttpClient,
    raw_ws_id: &str,
    user_id: &str,
    access_token: &str,
) -> Result<String, ()> {
    let resolved_ws_id = resolve_workspace_id(raw_ws_id);

    if resolved_ws_id == ROOT_WORKSPACE_ID {
        return Ok(ROOT_WORKSPACE_ID.to_owned());
    }

    if raw_ws_id
        .trim()
        .eq_ignore_ascii_case(PERSONAL_WORKSPACE_SLUG)
    {
        return personal_workspace_id(contact_data, outbound, user_id, access_token).await;
    }

    if !is_workspace_uuid_literal(&resolved_ws_id) {
        let handle = raw_ws_id.trim().to_lowercase();
        if !is_direct_workspace_lookup_identifier(&handle) {
            return Ok(resolved_ws_id);
        }

        if let Some(workspace_id) =
            workspace_id_by_handle(contact_data, outbound, &handle, access_token).await?
        {
            return Ok(workspace_id);
        }
        if let Some(workspace_id) =
            workspace_id_by_handle_service_role(contact_data, outbound, &handle).await?
        {
            return Ok(workspace_id);
        }
    }

    Ok(resolved_ws_id)
}

async fn personal_workspace_id(
    contact_data: &contact::ContactDataConfig,
    outbound: &impl OutboundHttpClient,
    user_id: &str,
    access_token: &str,
) -> Result<String, ()> {
    let Some(url) = contact_data.rest_url(
        "workspaces",
        &[
            (
                "select",
                "id,workspace_members!inner(user_id,type)".to_owned(),
            ),
            ("personal", "eq.true".to_owned()),
            ("workspace_members.user_id", format!("eq.{user_id}")),
            ("workspace_members.type", "eq.MEMBER".to_owned()),
            ("limit", "1".to_owned()),
        ],
    ) else {
        return Err(());
    };
    let response = send_caller_rest_request(contact_data, outbound, &url, access_token).await?;

    if !(200..300).contains(&response.status) {
        return Err(());
    }

    response
        .json::<Vec<WorkspaceIdRow>>()
        .map_err(|_| ())?
        .into_iter()
        .next()
        .and_then(|row| row.id)
        .ok_or(())
}

async fn workspace_id_by_handle(
    contact_data: &contact::ContactDataConfig,
    outbound: &impl OutboundHttpClient,
    handle: &str,
    access_token: &str,
) -> Result<Option<String>, ()> {
    let Some(url) = contact_data.rest_url(
        "workspaces",
        &[
            ("select", "id".to_owned()),
            ("handle", format!("eq.{handle}")),
            ("limit", "1".to_owned()),
        ],
    ) else {
        return Err(());
    };
    let response = send_caller_rest_request(contact_data, outbound, &url, access_token).await?;

    if !(200..300).contains(&response.status) {
        return Ok(None);
    }

    Ok(response
        .json::<Vec<WorkspaceIdRow>>()
        .map_err(|_| ())?
        .into_iter()
        .next()
        .and_then(|row| row.id))
}

